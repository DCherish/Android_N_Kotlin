package chap02.section4

fun main() {
    var x = 4
    var y = 0b0000_1010
    var z = 0x0F

    println("x shl 2 -> ${x shl 2}") // 0000 0000 ... 0000 0100(2) -> 0000 0000 ... 0001 0000(2)
    println("x.inv() -> ${x.inv()}") // 0000 0000 ... 0000 0100(2) -> 1111 1111 ... 1111 1011(2)

    println("y shr 2 -> ${y / 4}, ${y shr 2}") // 0000 0000 ... 0000 1010(2) -> 0000 0000 ... 0000 0010(2)
    println("x shl 4 -> ${x * 16}, ${x shl 4}") // 0000 0000 ... 0000 0100(2) -> 0000 0000 ... 0100 0000(2)
    println("z shl 4 -> ${z * 16}, ${z shl 4}") // 0000 0000 ... 0000 1111(2) -> 0000 0000 ... 1111 0000(2)

    x = 64
    println("x shr 2 -> ${x / 4}, ${x shr 2}") // 0000 0000 ... 0000 0100(2) -> 0000 0000 ... 0000 0010(2)
}